//Valida condiciones como IF pero por casos.
var num = 1;

switch (num){
    case 1:
        console.log("Soy 1");
        break;
    case 10:
        console.log("Soy 10");
        break;
    case 100:
        console.log("Soy 100");
        break;
    default:
        console.log("No soy nada");
}